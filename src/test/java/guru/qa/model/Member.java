package guru.qa.model;

public class Member {
    String name;
    Integer age;
    String secretIdentity;
    String[] powers;

    public String getName() {
        return name;
    }

    public Integer getAge() {
        return age;
    }

    public String getSecretIdentity() {
        return secretIdentity;
    }

    public String[] getPowers() {
        return powers;
    }
}
